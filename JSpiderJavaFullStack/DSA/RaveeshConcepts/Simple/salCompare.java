package DSA.RaveeshConcepts.Simple;

import java.util.Comparator;

public class salCompare implements Comparator {

    @Override
    public int compare(Object arg0, Object arg1) {
        Emp e1 = (Emp)arg0;
        Emp e2 = (Emp)arg1;
        if (e1.salary > e2.salary) {
            return 1;
        }
        if (e1.salary < e2.salary) {
            return -1;
        }
        return 0;
    }
	
}
