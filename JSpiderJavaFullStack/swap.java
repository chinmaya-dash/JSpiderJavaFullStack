public class swap {

    public static void main(String[] args) {
        int i = 10;
        int j = 5;
        System.out.println(i);
        System.out.println(j);

        i = i + j;// 15
        j = i - j;// 10
        i = i - j;
        System.out.println(i);
        System.out.println(j);

    }
}

            